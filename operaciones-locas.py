# Definición de la variable 'nombre' como una cadena de texto
nombre = "Alejandro"

# Definición de la variable 'apellido' como una cadena de texto
apellido = " Torres"

# Imprime la identificación única de la variable 'nombre'
print(id(nombre))

# Imprime la concatenación de las variables 'nombre' y 'apellido'
print(nombre + apellido)

# Reasignación de la variable 'nombre' como un entero (12)
nombre = int(12)

# Imprime la identificación única de la variable 'nombre' después de la reasignación
print(id(nombre))

# Imprime el valor de la variable 'nombre' después de la reasignación
print(nombre)

# Definición de una lista llamada 'lista'
lista = ["Papa", "Gaseosa", "Hamburguesa", "Papaya"]

# Acceso al elemento en la posición 2 de la lista y asignación a la variable 'elpepe'
elpepe = lista[2]

# Imprime el valor de la variable 'elpepe'
print(elpepe)

# Operaciones aritméticas en una expresión y asignación a la variable 'el_pepe'
el_pepe = (1) + 2 + 3 + 4 * 5 + (100 * 100) / 1000

# Imprime el valor de la variable 'el_pepe'
print(el_pepe)
